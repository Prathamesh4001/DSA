public class largestelement{
    public static void main(String[] args){
        int arr[]={12,34,56,78,90,11,23};
        int largest=arr[0];
        for(int i=1;i<arr.length;i++){
            if(arr[i]>largest){
                largest=arr[i];
            }
        }
        System.out.println("The largest element is: "+largest);
    }
}